package different.types.handler.operations;

public class OperationsHandler {
	
	public int addTwoNumbers(int firstNumber, int secondNumber) {
		int result = firstNumber + secondNumber;
		return result;		
	}
	
	public double addTwoNumbers(double firstNumber, double secondNumber) {
		double result = firstNumber + secondNumber;
		return result;
	}
	
	public int subtractTwoNumbers(int firstNumber, int secondNumber) {
		int result = firstNumber - secondNumber;
		return result;
	}
	
	public double subtractTwoNumbers(double firstNumber, double secondNumber) {
		double result = firstNumber - secondNumber;
		return result;
	}
	
	public int multiplyTwoNumbers(int firstNumber, int secondNumber) {
		int result = firstNumber * secondNumber;
		return result;
	}
	
	public double multiplyTwoNumbers(double firstNumber, double secondNumber) {
		double result = firstNumber * secondNumber;
		return result;
	}
	
	public int divideTwoNumbers(int firstNumber, int secondNumber) {
		int result = firstNumber / secondNumber;
		return result;
	}
	
	public double divideTwoNumbers(double firstNumber, double secondNumber) {
		double result = firstNumber / secondNumber;
		return result;
	}

}
